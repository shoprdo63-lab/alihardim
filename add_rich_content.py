def add_rich_content():
    app = create_app()
    
    with app.app_context():
        print("=" * 60)
        print("Adding Rich Content to Products for AdSense")
        print("=" * 60)
        
        products = Product.query.all()
        total = len(products)
        print(f"Total products: {total}")
        
        updated = 0
        for i, product in enumerate(products, 1):
            category = product.category
            if category in RICH_CONTENT:
                content = RICH_CONTENT[category]
                
                # Generate rich description
                rich_desc = f"""{product.title_hebrew or product.title} - מוצר איכותי שנבחר בקפידה עבורך.

**יתרונות המוצר:**
{chr(10).join(['• ' + p for p in content['pros'][:3]])}

**למה לקנות ב-AliExpress?**
{content['why_buy']}

**השוואת מחירים:**
מחיר ממוצע בישראל: ₪{int(product.price * content['israel_price_factor'] * 3.5)}
מחיר ב-AliExpress: ₪{int(product.price * 3.5)}
חיסכון: {int((1 - 1/content['israel_price_factor']) * 100)}%

**התאמה לציבור הדתי:**
מוצר זה עבר סינון קפדני ומותאם לערכי הצניעות של הקהילה החרדית.
"""
                
                product.description_hebrew = rich_desc
                updated += 1
                
                if i % 100 == 0:
                    print(f"  Updated {i}/{total} products...")
                    db.session.commit()
        
        db.session.commit()
        print(f"\n{'='*60}")
        print(f"Updated {updated} products with rich content!")
        print(f"{'='*60}")


if __name__ == '__main__':
    add_rich_content()
